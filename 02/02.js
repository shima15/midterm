function plus(fact1, fact2){
    var result = fact1 + fact2;
    return result;
}

function minus(fact1, fact2){
    var result = fact1 - fact2;
    return result;
}

function times(fact1, fact2){
    var result = fact1 * fact2;
    return result;
}

function div(fact1, fact2){
    var result = fact1 / fact2;
    return result;
}

function mod(fact1, fact2){
    var result = fact1 % fact2;
    return result;
}